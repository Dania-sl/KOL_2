'''
Знайти добуток елементів лінійного масиву цілих чисел, які кратні 5.
Розмірність масиву - 10. Заповнення масиву здійснити випадковими числами від 10 до
100.
'''
import numpy as np

X = np.random.randint(10, 100, 10)
s = 1
for i in X:
    if i % 5 == 0:
        s *= i
print(X)
print(s)
