'''
Перетин даху має форму півкола з радіусом R м.
Сформувати таблицю, яка містить довжини опор, які встановлюються через кожні R / 5 м.
 '''
import math

R = int(input('Enter radius: '))
l = R / 5
x = 0
i = 0
while x < 2 * R - l:
    x += l
    i += 1
    print(f'{i} = {math.sqrt(x * (2 * R - x)): .2f}')
